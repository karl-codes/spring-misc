package cookie;

public class OatmealRaisinCookie extends Cookie {

	private int raisinCount;
	
	private int oatMealPortions;

	/**
	 * @return the raisinCount
	 */
	public int getRaisinCount() {
		return raisinCount;
	}

	/**
	 * @param raisinCount the raisinCount to set
	 */
	public void setRaisinCount(int raisinCount) {
		this.raisinCount = raisinCount;
	}

	/**
	 * @return the oatMealPortions
	 */
	public int getOatMealPortions() {
		return oatMealPortions;
	}

	/**
	 * @param oatMealPortions the oatMealPortions to set
	 */
	public void setOatMealPortions(int oatMealPortions) {
		this.oatMealPortions = oatMealPortions;
	}
	
}
