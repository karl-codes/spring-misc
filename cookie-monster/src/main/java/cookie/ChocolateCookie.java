package cookie;

public class ChocolateCookie extends Cookie {
	
	private long chipCount;

	/**
	 * @return the chipCount
	 */
	public long getChipCount() {
		return chipCount;
	}

	/**
	 * @param chipCount
	 *            the chipCount to set
	 */
	public void setChipCount(long chipCount) {
		this.chipCount = chipCount;
	}
}